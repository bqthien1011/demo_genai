// Helper function to get products by tags
export const getProductsByTags = (tags: string[]): Product[] => {
  return mockProducts.filter((product) =>
    tags.some((tag) => product.tags.includes(tag.toLowerCase()))
  );
};

// Helper function to search products by query
export const searchProducts = (query: string): Product[] => {
  const lowerQuery = query.toLowerCase();
  return mockProducts.filter(
    (product) =>
      product.name.toLowerCase().includes(lowerQuery) ||
      product.description.toLowerCase().includes(lowerQuery) ||
      product.tags.some((tag: string) => tag.toLowerCase().includes(lowerQuery))
  );
};
